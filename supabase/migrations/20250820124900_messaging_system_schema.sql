-- 1. CONVERSATIONS TABLE (for both DMs and group chats)
-- =====================================================
CREATE TABLE public.conversations (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type text CHECK (type IN ('dm', 'group')) NOT NULL,
  name text, -- For group chats (plan names)
  event_id bigint REFERENCES public.events(id) ON DELETE CASCADE, -- For group chats
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now(),
  last_message_at timestamp with time zone,
  last_message_preview text,
  avatar_url text -- For group chat images
);

-- 2. CONVERSATION PARTICIPANTS (who can see each conversation)
-- =====================================================
CREATE TABLE public.conversation_participants (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  conversation_id bigint REFERENCES public.conversations(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  joined_at timestamp with time zone DEFAULT now(),
  last_read_at timestamp with time zone,
  is_muted boolean DEFAULT false,
  UNIQUE(conversation_id, user_id)
);

-- 3. ENHANCED MESSAGES TABLE (supports both DMs and group chats)
-- =====================================================
ALTER TABLE public.messages 
ADD COLUMN IF NOT EXISTS conversation_id bigint REFERENCES public.conversations(id) ON DELETE CASCADE,
ADD COLUMN IF NOT EXISTS reply_to_id bigint REFERENCES public.messages(id),
ADD COLUMN IF NOT EXISTS is_edited boolean DEFAULT false,
ADD COLUMN IF NOT EXISTS edited_at timestamp with time zone,
ADD COLUMN IF NOT EXISTS is_deleted boolean DEFAULT false,
ADD COLUMN IF NOT EXISTS deleted_at timestamp with time zone,
ADD COLUMN IF NOT EXISTS message_type text CHECK (message_type IN ('text', 'image', 'location', 'system')) DEFAULT 'text',
ADD COLUMN IF NOT EXISTS metadata jsonb; -- For images, locations, etc.

-- Create index for conversation-based queries
CREATE INDEX IF NOT EXISTS idx_messages_conversation_id ON public.messages(conversation_id);
CREATE INDEX IF NOT EXISTS idx_messages_created_at ON public.messages(created_at DESC);

-- 4. FRIEND SYSTEM TABLES
-- =====================================================
CREATE TABLE public.friendships (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  requester_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  addressee_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  status text CHECK (status IN ('pending', 'accepted', 'declined', 'blocked')) NOT NULL,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now(),
  UNIQUE(requester_id, addressee_id)
);

-- Index for fast friendship lookups
CREATE INDEX IF NOT EXISTS idx_friendships_requester ON public.friendships(requester_id);
CREATE INDEX IF NOT EXISTS idx_friendships_addressee ON public.friendships(addressee_id);
CREATE INDEX IF NOT EXISTS idx_friendships_status ON public.friendships(status);

-- 5. USER PRIVACY SETTINGS
-- =====================================================
CREATE TABLE public.user_privacy_settings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE UNIQUE,
  message_privacy text CHECK (message_privacy IN ('everyone', 'friends_only', 'nobody')) DEFAULT 'everyone',
  profile_visibility text CHECK (profile_visibility IN ('public', 'friends_only', 'private')) DEFAULT 'public',
  show_online_status boolean DEFAULT true,
  show_read_receipts boolean DEFAULT true,
  allow_friend_requests boolean DEFAULT true,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

-- 6. MESSAGE READ RECEIPTS
-- =====================================================
CREATE TABLE public.message_read_receipts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  message_id bigint REFERENCES public.messages(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  read_at timestamp with time zone DEFAULT now(),
  UNIQUE(message_id, user_id)
);

-- 7. TYPING INDICATORS
-- =====================================================
CREATE TABLE public.typing_indicators (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  conversation_id bigint REFERENCES public.conversations(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  started_at timestamp with time zone DEFAULT now(),
  UNIQUE(conversation_id, user_id)
);

-- 8. BLOCKED USERS
-- =====================================================
CREATE TABLE public.blocked_users (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  blocker_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  blocked_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  created_at timestamp with time zone DEFAULT now(),
  reason text,
  UNIQUE(blocker_id, blocked_id)
);

