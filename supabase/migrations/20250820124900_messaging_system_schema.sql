-- Function to auto-create conversation when event is created
CREATE OR REPLACE FUNCTION create_event_conversation()
RETURNS trigger AS $$
BEGIN
  -- Create a group conversation for the event
  INSERT INTO conversations (type, name, event_id, avatar_url)
  VALUES ('group', NEW.title, NEW.id, NEW.image_uri);
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER on_event_created
  AFTER INSERT ON events
  FOR EACH ROW
  EXECUTE FUNCTION create_event_conversation();

-- Function to add user to conversation when they join an event
CREATE OR REPLACE FUNCTION add_user_to_event_conversation()
RETURNS trigger AS $$
DECLARE
  conv_id bigint;
BEGIN
  -- Find the conversation for this event
  SELECT id INTO conv_id FROM conversations WHERE event_id = NEW.event_id;
  
  -- Add user as participant
  IF conv_id IS NOT NULL THEN
    INSERT INTO conversation_participants (conversation_id, user_id)
    VALUES (conv_id, NEW.user_id)
    ON CONFLICT DO NOTHING;
  END IF;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER on_attendance_created
  AFTER INSERT ON attendance
  FOR EACH ROW
  EXECUTE FUNCTION add_user_to_event_conversation();

-- Function to create/get DM conversation
CREATE OR REPLACE FUNCTION get_or_create_dm_conversation(
  user1_id uuid,
  user2_id uuid
)
RETURNS bigint AS $$
DECLARE
  conv_id bigint;
BEGIN
  -- Check if DM conversation already exists
  SELECT c.id INTO conv_id
  FROM conversations c
  WHERE c.type = 'dm'
  AND EXISTS (
    SELECT 1 FROM conversation_participants WHERE conversation_id = c.id AND user_id = user1_id
  )
  AND EXISTS (
    SELECT 1 FROM conversation_participants WHERE conversation_id = c.id AND user_id = user2_id
  );
  
  -- If not, create new DM conversation
  IF conv_id IS NULL THEN
    INSERT INTO conversations (type) VALUES ('dm') RETURNING id INTO conv_id;
    
    -- Add both participants
    INSERT INTO conversation_participants (conversation_id, user_id)
    VALUES (conv_id, user1_id), (conv_id, user2_id);
  END IF;
  
  RETURN conv_id;
END;
$$ LANGUAGE plpgsql;

-- Function to update conversation's last message
CREATE OR REPLACE FUNCTION update_conversation_last_message()
RETURNS trigger AS $$
BEGIN
  UPDATE conversations
  SET 
    last_message_at = NEW.created_at,
    last_message_preview = LEFT(NEW.content, 100),
    updated_at = now()
  WHERE id = NEW.conversation_id;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER on_message_sent
  AFTER INSERT ON messages
  FOR EACH ROW
  WHEN (NEW.is_deleted = false)
  EXECUTE FUNCTION update_conversation_last_message();

-- Function to check if users can message each other
CREATE OR REPLACE FUNCTION can_users_message(
  sender_id uuid,
  receiver_id uuid
)
RETURNS boolean AS $$
DECLARE
  privacy_setting text;
  are_friends boolean;
  is_blocked boolean;
BEGIN
  -- Check if blocked
  SELECT EXISTS (
    SELECT 1 FROM blocked_users 
    WHERE (blocker_id = receiver_id AND blocked_id = sender_id)
    OR (blocker_id = sender_id AND blocked_id = receiver_id)
  ) INTO is_blocked;
  
  IF is_blocked THEN
    RETURN false;
  END IF;
  
  -- Get receiver's privacy setting
  SELECT message_privacy INTO privacy_setting
  FROM user_privacy_settings
  WHERE user_id = receiver_id;
  
  -- Default to 'everyone' if no setting exists
  IF privacy_setting IS NULL THEN
    privacy_setting := 'everyone';
  END IF;
  
  IF privacy_setting = 'nobody' THEN
    RETURN false;
  ELSIF privacy_setting = 'everyone' THEN
    RETURN true;
  ELSE -- friends_only
    SELECT EXISTS (
      SELECT 1 FROM friendships
      WHERE status = 'accepted'
      AND ((requester_id = sender_id AND addressee_id = receiver_id)
      OR (requester_id = receiver_id AND addressee_id = sender_id))
    ) INTO are_friends;
    
    RETURN are_friends;
  END IF;
END;
$$ LANGUAGE plpgsql;

-- Function to get pending friend requests count
CREATE OR REPLACE FUNCTION get_pending_requests_count(user_id uuid)
RETURNS bigint AS $$
BEGIN
  RETURN (
    SELECT COUNT(*)
    FROM friendships
    WHERE addressee_id = user_id
    AND status = 'pending'
  );
END;
$$ LANGUAGE plpgsql;

-- Clean up old typing indicators (run periodically)
CREATE OR REPLACE FUNCTION cleanup_typing_indicators()
RETURNS void AS $$
BEGIN
  DELETE FROM typing_indicators
  WHERE started_at < now() - interval '30 seconds';
END;
$$ LANGUAGE plpgsql;